    private void tampilProduk(String idProduk) {
    try {
        koneksi k = new koneksi();
        k.koneksi();

        String sql = "SELECT * FROM produk WHERE id_produk=?";
        PreparedStatement pst = k.con.prepareStatement(sql);
        pst.setString(1, idProduk);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            txtIdProduk.setText(rs.getString("id_produk"));
            txtNamaProduk.setText(rs.getString("nama_produk"));
            txtHarga.setText(rs.getString("harga"));
            txtStok.setText(rs.getString("stok"));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
