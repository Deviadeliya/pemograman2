    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
                                           
    try {
        koneksi k = new koneksi();
        k.koneksi();

        String idProduk = jTextFieldIdProduk.getText();
        int jumlah = Integer.parseInt(jTextFieldJumlah.getText());

        String sqlHarga = "SELECT harga FROM produk WHERE id_produk=?";
        PreparedStatement pstHarga = k.con.prepareStatement(sqlHarga);
        pstHarga.setString(1, idProduk);
        ResultSet rs = pstHarga.executeQuery();

        if (rs.next()) {
            int harga = rs.getInt("harga");
            int total = harga * jumlah;

            String sql = "INSERT INTO tbtransaksi (id_produk, jumlah, tanggal, total) VALUES (?,?,?,?)";
            PreparedStatement pst = k.con.prepareStatement(sql);
            pst.setString(1, idProduk);
            pst.setInt(2, jumlah);
            pst.setString(3, jTextFieldTanggal.getText());
            pst.setInt(4, total);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Transaksi berhasil");
            tampilTransaksi();
        } else {
            JOptionPane.showMessageDialog(this, "Produk tidak ditemukan");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

 
    }//GEN-LAST:event_jButton4ActionPerformed

